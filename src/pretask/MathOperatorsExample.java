package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int a = 35;
        int b = 7;
        int sum = a + b;
        int minus = a - b;
        int mul = a * b;
        int div = a / b;
        System.out.println("a+b=" + sum);
        System.out.println("a-b=" + minus);
        System.out.println("a*b=" + mul);
        System.out.println("a/b=" + div);

    }
}
